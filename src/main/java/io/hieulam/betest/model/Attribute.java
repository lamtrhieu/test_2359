package io.hieulam.betest.model;

public class Attribute {
    private String name;
    private String unit;
    private String type;
    private String value;

    public Attribute(String name, String unit, String type, String value) {
        this.name = name;
        this.unit = unit;
        this.type = type;
        this.value = value;
    }




}
